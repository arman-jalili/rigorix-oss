// ---------------------------------------------------------------------------
// ValidatePlan DTOs
// ---------------------------------------------------------------------------

/// Input for standalone plan validation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidatePlanInput {
    /// The execution ID.
    pub execution_id: Uuid,

    /// The TaskGraph to validate.
    pub graph: TaskGraph,

    /// The template ID that generated this graph.
    pub template_id: String,

    /// Whether to check all validation rules or just structural ones.
    #[serde(default = "default_true")]
    pub full_validation: bool,
}

/// Output from standalone plan validation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidatePlanOutput {
    /// Whether the plan passed all validation checks.
    pub passed: bool,

    /// Validation errors (blocking).
    pub errors: Vec<ValidationError>,

    /// Validation warnings (non-blocking).
    pub warnings: Vec<ValidationWarning>,

    /// Number of checks performed.
    pub checks_performed: u32,
}

/// A validation error (blocking — plan cannot proceed).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidationError {
    /// Human-readable error message.
    pub message: String,

    /// The validation rule that failed.
    pub rule: String,

    /// The node ID that triggered the error (if applicable).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub node_id: Option<String>,
}

/// A validation warning (non-blocking — plan can proceed).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidationWarning {
    /// Human-readable warning message.
    pub message: String,

    /// The validation rule that triggered this warning.
    pub rule: String,

    /// The node ID that triggered the warning (if applicable).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub node_id: Option<String>,
}

// ---------------------------------------------------------------------------
// RequestClarification DTOs
// ---------------------------------------------------------------------------

/// Input for requesting clarification from the user.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RequestClarificationInput {
    /// The execution ID.
    pub execution_id: Uuid,

    /// The user's current intent with previous clarifications.
    pub intent: UserIntent,

    /// The classification result that triggered the clarification.
    pub classification: ClassificationResult,

    /// Optional specific question to ask (generated if not provided).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub custom_question: Option<String>,
}

/// Output from requesting clarification.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RequestClarificationOutput {
    /// The question to present to the user.
    pub question: String,

    /// The ambiguous templates that need disambiguation.
    pub ambiguous_templates: Vec<ClassifiedTemplate>,

    /// Suggested answers or options for the user.
    #[serde(default)]
    pub suggested_answers: Vec<String>,
}

// ---------------------------------------------------------------------------
// AvailableTemplates DTO
// ---------------------------------------------------------------------------

/// Output from listing available templates.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AvailableTemplatesOutput {
    /// Available templates with metadata.
    pub templates: Vec<TemplateSummary>,

    /// Total number of registered templates.
    pub total_count: u32,
}

// ---------------------------------------------------------------------------
// Clarification Round DTO
// ---------------------------------------------------------------------------

/// A single round of clarification with the user.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ClarificationRound {
    /// The question asked to the user.
    pub question: String,

    /// The user's response.
    pub answer: String,

    /// The classification result after this round.
    pub classification: ClassificationResult,

    /// ISO 8601 timestamp.
    pub timestamp: DateTime<Utc>,
}
