/// Input for budget pre-check.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CheckBudgetInput {
    /// The execution ID.
    pub execution_id: Uuid,

    /// Number of LLM calls required (defaults to 2 for classify + extract).
    #[serde(default = "default_min_calls")]
    pub required_calls: u32,
}

fn default_min_calls() -> u32 {
    2
}

/// Output from budget pre-check.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CheckBudgetOutput {
    /// Whether the budget has sufficient capacity.
    pub has_capacity: bool,

    /// Remaining LLM calls.
    pub remaining_calls: u32,

    /// Remaining LLM tokens.
    pub remaining_tokens: u32,

    /// Maximum allowed calls.
    pub max_calls: u32,

    /// Maximum allowed tokens.
    pub max_tokens: u32,

    /// Whether the budget will be exhausted after this planning operation.
    pub will_exhaust: bool,
}

// ---------------------------------------------------------------------------
// ExtractParameters DTOs
// ---------------------------------------------------------------------------

/// Input for standalone parameter extraction.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExtractParametersInput {
    /// The execution ID.
    pub execution_id: Uuid,

    /// The user intent with optional clarifications.
    pub intent: UserIntent,

    /// The template ID to extract parameters for.
    pub template_id: String,

    /// The list of parameter names to extract.
    pub parameter_names: Vec<String>,
}

/// Output from standalone parameter extraction.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExtractParametersOutput {
    /// The template ID.
    pub template_id: String,

    /// Resolved key-value parameters.
    pub parameters: HashMap<String, String>,

    /// Extra parameters found but not defined.
    #[serde(default)]
    pub extra_parameters: HashMap<String, String>,

    /// Missing required parameters.
    #[serde(default)]
    pub missing_parameters: Vec<String>,

    /// Whether all required parameters were extracted.
    pub complete: bool,

    /// LLM calls used.
    pub llm_calls_used: u32,

    /// LLM tokens used.
    pub llm_tokens_used: u32,
}

